"""Ninja build file generator.

Generates build.ninja files from the project config and dependency graph,
including compile rules, link rules, glob expansion, and
compile_commands.json for IDE integration.
"""

from __future__ import annotations

import glob
import json
import os
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, Dict, List, Optional

from ebuild.build.compiler import Compiler, Language, _detect_language
from ebuild.build.toolchain import resolve_toolchain
from ebuild.core.config import ProjectConfig, TargetConfig
from ebuild.core.graph import DependencyGraph, build_dependency_graph


@dataclass
class PackagePaths:
    """Resolved include/lib paths for installed packages."""

    include_dirs: List[Path] = field(default_factory=list)
    lib_dirs: List[Path] = field(default_factory=list)
    libraries: List[str] = field(default_factory=list)


def _expand_sources(patterns: List[str], base_dir: Path) -> List[Path]:
    """Expand source glob patterns relative to base_dir."""
    sources: List[Path] = []
    for pattern in patterns:
        full_pattern = str(base_dir / pattern)
        matches = sorted(glob.glob(full_pattern, recursive=True))
        if not matches:
            single = base_dir / pattern
            if single.exists():
                sources.append(single)
        else:
            sources.extend(Path(m) for m in matches)
    return sources


def _obj_path(source: Path, build_dir: Path, target_name: str) -> Path:
    """Compute the object file path for a given source file."""
    stem = source.stem
    return build_dir / target_name / f"{stem}.o"


def _depfile_path(obj: Path) -> Path:
    """Compute the .d dependency file path for a given object."""
    return obj.with_suffix(".d")


def _output_name(target: TargetConfig) -> str:
    """Compute the output filename for a target."""
    if target.target_type == "executable":
        return target.name
    elif target.target_type == "static_library":
        return f"lib{target.name}.a"
    elif target.target_type == "shared_library":
        return f"lib{target.name}.so"
    return target.name


class NinjaBackend:
    """Generates build.ninja from a ProjectConfig."""

    def __init__(
        self,
        config: ProjectConfig,
        build_dir: Path,
        compiler: Optional[Compiler] = None,
        package_paths: Optional[Dict[str, PackagePaths]] = None,
    ) -> None:
        self.config = config
        self.build_dir = build_dir
        self.source_dir = config.source_dir
        self.compiler = compiler or resolve_toolchain(config.toolchain)
        self.graph = build_dependency_graph(config.targets)
        self.package_paths = package_paths or {}
        self._compile_commands: List[Dict[str, str]] = []

    def generate(self) -> str:
        """Generate the full build.ninja content and compile_commands.json."""
        lines: List[str] = []
        lines.append("# Auto-generated by ebuild — do not edit")
        lines.append(f"# Project: {self.config.name} v{self.config.version}")
        lines.append("")

        lines.extend(self._write_variables())
        lines.append("")
        lines.extend(self._write_rules())
        lines.append("")

        build_order = self.graph.topological_sort()
        for target_name in build_order:
            target = self.config.get_target(target_name)
            if target is None:
                continue
            lines.extend(self._write_target(target))
            lines.append("")

        ninja_content = "\n".join(lines)

        self.build_dir.mkdir(parents=True, exist_ok=True)
        ninja_path = self.build_dir / "build.ninja"
        ninja_path.write_text(ninja_content, encoding="utf-8")

        self._write_compile_commands()

        return ninja_content

    def _write_variables(self) -> List[str]:
        lines = []
        lines.append(f"builddir = {self.build_dir}")
        return lines

    def _write_rules(self) -> List[str]:
        lines = []

        lines.append("rule cc")
        lines.append(f"  command = {self.compiler.cc} $cflags -MD -MF $out.d -c $in -o $out")
        lines.append("  depfile = $out.d")
        lines.append("  deps = gcc")
        lines.append("  description = CC $in")
        lines.append("")

        lines.append("rule cxx")
        lines.append(f"  command = {self.compiler.cxx} $cflags -MD -MF $out.d -c $in -o $out")
        lines.append("  depfile = $out.d")
        lines.append("  deps = gcc")
        lines.append("  description = CXX $in")
        lines.append("")

        lines.append("rule link")
        lines.append(f"  command = {self.compiler.cc} $ldflags $in -o $out $libs")
        lines.append("  description = LINK $out")
        lines.append("")

        lines.append("rule link_cxx")
        lines.append(f"  command = {self.compiler.cxx} $ldflags $in -o $out $libs")
        lines.append("  description = LINK $out")
        lines.append("")

        lines.append("rule ar")
        lines.append(f"  command = {self.compiler.ar} rcs $out $in")
        lines.append("  description = AR $out")
        lines.append("")

        lines.append("rule shared")
        lines.append(f"  command = {self.compiler.cc} -shared $ldflags $in -o $out $libs")
        lines.append("  description = SHARED $out")

        return lines

    def _write_target(self, target: TargetConfig) -> List[str]:
        lines: List[str] = []
        lines.append(f"# Target: {target.name} ({target.target_type})")

        sources = _expand_sources(target.sources, self.source_dir)
        if not sources:
            lines.append(f"# WARNING: no source files found for target '{target.name}'")
            return lines

        includes = [str(self.source_dir / inc) for inc in target.includes]

        # Add include dirs from packages this target uses
        for pkg_name in getattr(target, "uses", []):
            pkg = self.package_paths.get(pkg_name)
            if pkg:
                for inc_dir in pkg.include_dirs:
                    includes.append(str(inc_dir))

        include_flags = " ".join(f"-I{inc}" for inc in includes)
        cflags_str = " ".join(target.cflags)
        all_cflags = f"{include_flags} {cflags_str}".strip()

        obj_files: List[str] = []
        for src in sources:
            obj = _obj_path(src, self.build_dir, target.name)
            obj_files.append(str(obj))

            lang = _detect_language(str(src))
            rule = "cxx" if lang == Language.CPP else "cc"

            lines.append(f"build {obj}: {rule} {src}")
            if all_cflags:
                lines.append(f"  cflags = {all_cflags}")

            self._compile_commands.append({
                "directory": str(self.source_dir.resolve()),
                "command": f"{self.compiler.cc} {all_cflags} -c {src} -o {obj}",
                "file": str(src),
            })

        output_name = _output_name(target)
        output_path = self.build_dir / output_name

        dep_libs: List[str] = []
        dep_outputs: List[str] = []
        for dep_name in target.depends:
            dep_target = self.config.get_target(dep_name)
            if dep_target:
                dep_out = str(self.build_dir / _output_name(dep_target))
                dep_outputs.append(dep_out)
                if dep_target.target_type == "static_library":
                    dep_libs.append(dep_out)
                elif dep_target.target_type == "shared_library":
                    dep_libs.append(f"-L{self.build_dir}")
                    dep_libs.append(f"-l{dep_name}")

        # Add lib dirs and libraries from packages this target uses
        pkg_ldflags: List[str] = []
        pkg_libs: List[str] = []
        for pkg_name in getattr(target, "uses", []):
            pkg = self.package_paths.get(pkg_name)
            if pkg:
                for lib_dir in pkg.lib_dirs:
                    pkg_ldflags.append(f"-L{lib_dir}")
                for lib in pkg.libraries:
                    pkg_libs.append(f"-l{lib}")

        ldflags_str = " ".join(target.ldflags + pkg_ldflags)
        libs_str = " ".join(dep_libs + pkg_libs)
        obj_list = " ".join(obj_files)

        if target.target_type == "executable":
            implicit = f" | {' '.join(dep_outputs)}" if dep_outputs else ""
            lines.append(f"build {output_path}: link {obj_list}{implicit}")
            if ldflags_str:
                lines.append(f"  ldflags = {ldflags_str}")
            if libs_str:
                lines.append(f"  libs = {libs_str}")

        elif target.target_type == "static_library":
            lines.append(f"build {output_path}: ar {obj_list}")

        elif target.target_type == "shared_library":
            implicit = f" | {' '.join(dep_outputs)}" if dep_outputs else ""
            lines.append(f"build {output_path}: shared {obj_list}{implicit}")
            if ldflags_str:
                lines.append(f"  ldflags = {ldflags_str}")
            if libs_str:
                lines.append(f"  libs = {libs_str}")

        return lines

    def _write_compile_commands(self) -> None:
        """Write compile_commands.json for IDE integration."""
        cc_path = self.build_dir / "compile_commands.json"
        cc_path.write_text(
            json.dumps(self._compile_commands, indent=2),
            encoding="utf-8",
        )
